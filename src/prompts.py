def load_school_info() -> str:
    """读取data/目录下所有md文件，拼成一段大文本
    用通配符读取，后续新增md文件不用改代码
    """
    data_dir = Path("data")
    md_files = sorted(data_dir.glob("*.md"))
    contents = []
    for f in md_files:
        content = f.read_text(encoding="utf-8")
        contents.append(f"=== {f.name} ===\n{content}")
    return "\n\n".join(contents)


def get_system_prompt(role: str, school_info: str) -> str:
    """组装完整的system prompt：身份角色 + 别名词典 + 硬规则 + 学校资料"""
    role_text = ROLE_PROMPTS.get(role, ROLE_PROMPTS["在校生"])
    return f"""你是"小航"，郑州航空工业管理学院的校园信息查询AI助手。
{role_text}
{ALIAS_DICT}
{HARD_RULES}
【学校资料】
{school_info}
"""
